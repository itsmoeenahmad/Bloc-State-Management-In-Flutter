Bloc stands for Business Logic Component.
Bloc consists of two main things which is events & states.
Bloc State management work like that: Event -> Bloc -> State

Event is generated when user click on UI and send to Bloc where main Logic code is written for the
problem solution and state is the result which is shown on screen.

Its files structure like:
problemName_event.dart
problemName_bloc.dart
problemName_state.dart

For Counter App It is like:

counter_event.dart
counter_bloc.dart
counter_state.dart



Install Bloc Plugin for which will automatically make your three files easily in one click.

